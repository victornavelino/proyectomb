    private void inicializarComponentes() {
        this.setTitle("Autorizar descuento");
        lblDescuento.setEnabled(false);
        tfDescuento.setEnabled(false);
        tfUsuario.setText("descuento");
        passContrasena.requestFocus();
        jXBusyLabel1.setVisible(false);

    }

    public void corroborarContrasena() {

        try {
            SwingWorker worker = new SwingWorker() {
                Usuario usuario = new Usuario();

                @Override
                protected Object doInBackground() throws Exception {
                    jXBusyLabel1.setVisible(true);
                    usuario.setNombreUsuario(tfUsuario.getText());

                    usuario.setContrasena(new String(passContrasena.getPassword()));
                    usuario = UsuarioFacade.getInstance().validar(usuario);
                    return null;
                }

                @Override
                protected void done() {
                    if (usuario.getId() != null && usuario.getNombreUsuario().equals("descuento")) {
                        tfDescuento.setEnabled(true);
                        lblDescuento.setEnabled(true);
                        tfDescuento.requestFocus();

                    } else {
                        JOptionPane.showMessageDialog(null, "Nombre de Usuario y/o "
                                + "Contraseña incorrecta para realizar descuento",
                                "Error", JOptionPane.ERROR_MESSAGE);
                    }
                    jXBusyLabel1.setVisible(false);
                }
            };
            worker.execute();
        } catch (DatabaseException ex) {
            JOptionPane.showMessageDialog(null, "No se puede establecer una "
                    + "conexión con el servidor", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void aceptar() {

        if (validar()) {
            this.setDescuento(BigDecimal.valueOf(Double.parseDouble(tfDescuento.getText())));
            this.dispose();
        }
    }

    private boolean validar() {
        if (tfDescuento.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar el porcentaje de descuento");
            return false;
        }
        return true;

    }
