    private void buscarUltimoCierreCaja() {
        try {
            tfNumeroTIcket.setText(String.valueOf(VentaFacade.getInstance().getUltimoNumeroTicket()));
        } catch (Exception e) {

        }

    }

    private void reimprimir() {
        if (!tfNumeroTIcket.getText().isEmpty() && Comunes.validarInteger(tfNumeroTIcket.getText())) {
            Venta ventaSeleccionada = VentaFacade.getInstance().getVentaNumeroTicket(tfNumeroTIcket.getText());
            if (ventaSeleccionada != null) {
                //IF AGREGADO PARA DIFERENCIAR TICKET CARNICERIA CON TICKET SANDWICHERIA
                if (ventaSeleccionada.getSucursal().getNombre().contains("LA HERENCIA")) {
                    new Impresora().imprimirTicketSandwicheria(ventaSeleccionada);
                } else {
                    new Impresora().imprimir(ventaSeleccionada);
                }
                new Impresora().imprimir(ventaSeleccionada);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "No existe ese numero de caja", "Mensaje de error", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Debe escribir un numero de cierre de Caja");
            tfNumeroTIcket.requestFocus();
        }
    }
