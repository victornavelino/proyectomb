    private void agregarNuevaPersona() {
        DiagEmpleadoPersona diagClientePersona = new DiagEmpleadoPersona(null, true, "Empleado", "AltaPersona");
        diagClientePersona.setLocation(Comunes.centrarDialog(diagClientePersona));
        diagClientePersona.setVisible(true);
        cargarTablaEmpleados(EmpleadoFacade.getInstance().getTodosOrdenadosXApellido());
    }

    private void cargarTablaEmpleados(List<Empleado> empleados) {
        modeloTablaEmpleados = new ModeloTablaNoEditable();
        cargarEncabezadosTablaEmpleados(modeloTablaEmpleados);
        configurarTabla(tblEmpleados);
        try {
            cargarEmpleados(empleados);
        } catch (Exception ex) {

        }
    }

    private void cargarEncabezadosTablaEmpleados(ModeloTablaNoEditable modeloTablaPersonas) {
        modeloTablaPersonas.addColumn("Id");
        modeloTablaPersonas.addColumn("Apellido");
        modeloTablaPersonas.addColumn("Nombre");
        modeloTablaPersonas.addColumn("Documento");
        modeloTablaPersonas.addColumn("Tipo Empleado");
        tblEmpleados.setModel(modeloTablaPersonas);
         Comunes.setOcultarColumnasJTable(tblEmpleados, 0);
    }

    private void configurarTabla(JTable tbl) {
        JViewport scroll = (JViewport) tbl.getParent();
        int ancho = scroll.getWidth();
        int anchoColumna = 0;
        TableColumnModel modeloColumna = tbl.getColumnModel();
        TableColumn columnaTabla;
        for (int i = 0; i < tbl.getColumnCount(); i++) {
            columnaTabla = modeloColumna.getColumn(i);
            switch (i) {
                case 0:
                    anchoColumna = (1 * ancho) / 100;
                    break;
                case 1:
                    anchoColumna = (20 * ancho) / 100;
                    break;
                case 2:
                case 3:
                case 4:
                case 5:
                case 6:
                case 7:
                case 8:
                case 9:
                case 10:
                case 11:
                case 12:
                    anchoColumna = (5 * ancho) / 100;
                    break;
            }
            columnaTabla.setPreferredWidth(anchoColumna);
            tbl.setColumnModel(modeloColumna);
        }
        tbl.getTableHeader().setFont(new java.awt.Font("Dialog",
                java.awt.Font.PLAIN, 10));
        tbl.getTableHeader().setBackground(java.awt.Color.WHITE);
        tbl.getTableHeader().setForeground(Color.BLACK);
        //Si le queremos cambiar el tamaño a la tablita
        tbl.setFont(new java.awt.Font("Dialog",
                java.awt.Font.PLAIN, 10));
    }

    private void cargarEmpleados(List<Empleado> empleados) {
        try {
            modeloTablaEmpleados = new ModeloTablaNoEditable();
            cargarEncabezadosTablaEmpleados(modeloTablaEmpleados);
            for (Empleado empleado : empleados) {
                cargarEmpleado(empleado);
            }

            tblEmpleados.setModel(modeloTablaEmpleados);
        } catch (Exception ex) {
            Logger.getLogger(DiagEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void cargarEmpleado(Empleado empleado) {

        Object[] fila = new Object[5];
        fila[0] = empleado.getId();
        fila[1] = empleado.getApellido();
        fila[2] = empleado.getNombre();
        fila[3] = empleado.getDocumentoIdentidad();
        fila[4] = empleado.getTipoEmpleado();
        modeloTablaEmpleados.addRow(fila);
    }

    private void modificarPersona() {
        if (tblEmpleados.getSelectedRow() != -1) {
            Empleado empleado = EmpleadoFacade.getInstance().getEmpleadoXId((Long) tblEmpleados.getValueAt(tblEmpleados.getSelectedRow(), 0));
            DiagEmpleadoPersona diagClientePersona = new DiagEmpleadoPersona(null, true, "Empleado", "ModificaciónEmpleado", empleado);
            diagClientePersona.setLocation(Comunes.centrarDialog(diagClientePersona));
            diagClientePersona.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una Empleado");
        }

    }

    private void eliminarPersona() {
        if (tblEmpleados.getSelectedRow() != -1) {
            int i = JOptionPane.showConfirmDialog(this, "¿Desea eliminar el Registro seleccionado?", "Confirmar eliminacion", JOptionPane.YES_NO_OPTION);
            if (i == 0) {
                EmpleadoFacade.getInstance().eliminar((Long) tblEmpleados.getValueAt(tblEmpleados.getSelectedRow(), 0));
                JOptionPane.showMessageDialog(null, "Registro eliminado");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un Empleado");
        }
    }

    private void buscarEmpleado() {
        if (!tfApellido.getText().isEmpty()) {
            cargarTablaEmpleados(EmpleadoFacade.getInstance().getEmpleadoNombre(tfApellido.getText()));
        }else{
            cargarTablaEmpleados(EmpleadoFacade.getInstance().getTodosOrdenadosXApellido());
        }
    }

