    private void inicializarComponentes() {
        this.setTitle("Categorias");
        Comunes.cargarJComboConBlanco(cbTipoIva, TipoIvaFacade.getInstance().getTodos());
        agregarCategoriaLista();
    }

    private void AltaCategoria() {
        if (validarCategoria()) {
            try {
                Categoria cat = new Categoria();
                cat.setDescripcion(tfCategoria.getText());
                cat.setTipoIva((TipoIva) cbTipoIva.getSelectedItem());
                CategoriaFacade.getInstance().alta(cat);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }

    private boolean validarCategoria() {
        if (tfCategoria.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar la Descripcion de la Categoria");
            tfCategoria.requestFocus();
            return false;
        } else if (cbTipoIva.getSelectedIndex() < 1) {
            JOptionPane.showMessageDialog(null, "Debe Seleccionar tipo Iva");
            cbTipoIva.requestFocus();
            return false;
        } else {
            return true;
        }
    }

    private void agregarCategoriaLista() {
        Comunes.cargarJList(jlCategorias, CategoriaFacade.getInstance().getTodos());
    }

    private void modificarCategoria() {
     if(jlCategorias.getSelectedIndex()!=-1){
         categoria.setDescripcion(tfCategoria.getText());
         categoria.setTipoIva((TipoIva)cbTipoIva.getSelectedItem());
         CategoriaFacade.getInstance().modificar(categoria);
         JOptionPane.showMessageDialog(null, "Categoria Modificada");
         tfCategoria.setText("");
         btnGuardar.setEnabled(true);
         agregarCategoriaLista();
     }
     else{
     JOptionPane.showMessageDialog(null, "Para poder modificar una Categoria debe Seleccionar una de La Lista");
     }
    }

    private void recogerCategoriaSeleccionada() {
        categoria =(Categoria)jlCategorias.getSelectedValue();
        tfCategoria.setText(categoria.getDescripcion());
        cbTipoIva.setSelectedItem(categoria.getTipoIva());
    }

    private void eliminarCategoria() {
        if(jlCategorias.getSelectedIndex()!=-1){
            int i = JOptionPane.showConfirmDialog(this, "¿Desea eliminar la Categoria seleccionada?", "Confirmar eliminacion", JOptionPane.YES_NO_OPTION);
            if (i == 0) {
               
                try {
                    CategoriaFacade.getInstance().eliminar(((Categoria) jlCategorias.getSelectedValue()).getId());
                    JOptionPane.showMessageDialog(null, "Registro eliminado");
                    agregarCategoriaLista();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "No se puede eliminar, categoria esta siendo utilizada por una subcategoria");
                }
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Debe seleccionar una Categoria para eliminar");
        }
    }
