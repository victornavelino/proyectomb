    private void inicializarComponentes() {
        listaPanelesAfuera = new ArrayList<>();
        listaPanelesAdentro = new ArrayList<>();
        Comunes.cargarJComboConBlanco(cboGrupos, GrupoFacade.getInstance().listarGruposExceptoAdministradores());
        listaPanelesAfuera = PanelesFacade.getInstance().getTodos();
        Comunes.cargarJList(jlistPanelesAfuera, listaPanelesAfuera);
        Comunes.cargarJList(jlistPanelesAdentro, new ArrayList());

    }

    private void cargarPermiso() {
        if (cboGrupos.getSelectedIndex() > 0) {
            grupo = (Grupo) cboGrupos.getSelectedItem();
            listaPanelesAdentro = grupo.getPaneles();
            Comunes.cargarJList(jlistPanelesAdentro, listaPanelesAdentro);
            listaPanelesAfuera.removeAll(listaPanelesAdentro);
            Comunes.cargarJList(jlistPanelesAfuera, listaPanelesAfuera);
        }

    }

    private void agregarPanel() {
        if (jlistPanelesAfuera.getSelectedIndex() != -1) {
            listaPanelesAdentro.add((Paneles) jlistPanelesAfuera.getSelectedValue());
            listaPanelesAfuera = PanelesFacade.getInstance().getTodos();
            listaPanelesAfuera.removeAll(listaPanelesAdentro);
            Comunes.cargarJList(jlistPanelesAfuera, listaPanelesAfuera);
            Comunes.cargarJList(jlistPanelesAdentro, listaPanelesAdentro);
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un elemento");
        }
    }

    private void quitarPanel() {
        if (jlistPanelesAdentro.getSelectedIndex() != -1) {
            listaPanelesAfuera.add((Paneles) jlistPanelesAdentro.getSelectedValue());
            listaPanelesAdentro = PanelesFacade.getInstance().getTodos();
            listaPanelesAdentro.removeAll(listaPanelesAfuera);
            Comunes.cargarJList(jlistPanelesAfuera, listaPanelesAfuera);
            Comunes.cargarJList(jlistPanelesAdentro, listaPanelesAdentro);
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un elemento");
        }
    }

    private void guardarDatos() {
        grupo.setPaneles(listaPanelesAdentro);
        GrupoFacade.getInstance().editar(grupo);
        JOptionPane.showMessageDialog(null, "Grupo Modificado");
        grupo = null;
        inicializarComponentes();
    }
