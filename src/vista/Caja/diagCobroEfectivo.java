    private void calcularVuelto() {
        if (!tfPagaCon.getText().isEmpty() && Comunes.validarBigDecimal(tfPagaCon.getText())) {
            if (new BigDecimal(tfPagaCon.getText().replace(",", ".")).signum() > 0
                    && new BigDecimal(tfTotal.getText().replace(",", ".")).signum() > 0) {
                //calculamos el vuelto
                vuelto = new BigDecimal(tfPagaCon.getText().replace(",", "."))
                        .subtract(new BigDecimal(tfTotal.getText().replace(",", ".")));
                if (vuelto.signum() > -1) {

                    ftfVuelto.setText(vuelto.toString());
                } else {
                    ftfVuelto.setText("");
                }

            }
        } else {
            ftfVuelto.setText("");
        }
    }

    private void aceptar() {
        if (validar()) {
            //este es sin los ajuste (datos cobrados sobre el ticket)
            aPagarEfectivo = BigDecimal.valueOf(Double.parseDouble(tfImporteAPagar.getText().replace(",", ".")));
            aPagarTotal=BigDecimal.valueOf(Double.parseDouble(tfTotal.getText().replace(",", ".")));
            this.dispose();
        }

    }

    private void inicializarComponentes() {
        //tfImporteOriginalEfectivo.setText(new DecimalFormat("0.00").format(monto));
        tfImporteOriginalEfectivo.setText(monto.toString());
        //tfImporteAPagar.setText(new DecimalFormat("0.00").format(monto));
        tfImporteAPagar.setText(monto.toString());
        tfImporteAPagar.requestFocus();
        //tfTotal.setText(new DecimalFormat("0.00").format(monto));
        tfTotal.setText(monto.toString());
        jLabel4.setVisible(false);
        tfAjuste.setVisible(false);
    }

    private boolean validar() {
        if (tfImporteAPagar.getText().isEmpty() && !Comunes.validarBigDecimal(tfImporteAPagar.getText())) {
            JOptionPane.showMessageDialog(null, "debe cargar un importe a pagar");
            tfImporteAPagar.requestFocus();
            return false;
        }
        if (monto.subtract(BigDecimal.valueOf(Double.parseDouble((tfImporteAPagar.getText().replace(",", "."))))).signum() == -1) {
            System.out.println("monto: "+monto);
            System.out.println("importe a pagar: "+tfImporteAPagar.getText());
            System.out.println("monto - importe a pagar: "+monto.subtract(BigDecimal.valueOf(Double.parseDouble((tfImporteAPagar.getText().replace(",", "."))))));
            JOptionPane.showMessageDialog(null, "Importe a pagar mayor que el saldo");
            tfImporteAPagar.requestFocus();
            return false;
        }
        return true;
    }

    private void calcular() {
        if (!tfPagaCon.getText().isEmpty()) {
            calcularVuelto();
        } else {
            ftfVuelto.setText("");
        }
    }

    private void calcularTotal() {
        if (!tfAjuste.getText().isEmpty() && !tfImporteAPagar.getText().isEmpty()) {
            tfTotal.setText(new DecimalFormat("0.00").format(new BigDecimal(tfImporteAPagar.getText().replace(",", "."))
                    .add(new BigDecimal(tfAjuste.getText().replace(",", ".")))));
        } else {
            tfTotal.setText(tfImporteAPagar.getText());
        }
    }

