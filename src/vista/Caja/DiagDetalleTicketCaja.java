    private void inicialziarComponentes() {
        cargarDetalleTicket(venta.getVentasArticulos());
    }

    private void cargarDetalleTicket(List<VentaArticulo> ventasArticulos) {
        modeloTablaDetalle = new ModeloTablaNoEditable();
        cargarEncabezadosTablaDetalle(modeloTablaDetalle);
        configurarTablaVentas(tblDetalle);
        try {
            cargarDetalle(venta.getVentasArticulos());
        } catch (Exception ex) {
            Logger.getLogger(DiagDetalleVenta.class.getName()).log(Level.SEVERE, null, ex);
        }
        Comunes.setOcultarColumnasJTable(tblDetalle, 0);
    }

    private void cargarDetalle(List<VentaArticulo> listaVentaArticulos) {
        try {
            modeloTablaDetalle = new ModeloTablaNoEditable();
            cargarEncabezadosTablaDetalle(modeloTablaDetalle);
            for (VentaArticulo ventaArt : listaVentaArticulos) {
                cargarArticulos(ventaArt);
            }
            tblDetalle.setModel(modeloTablaDetalle);
            Comunes.setOcultarColumnasJTable(tblDetalle, 0);
        } catch (Exception ex) {
            Logger.getLogger(DiagDetalleVenta.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void cargarArticulos(VentaArticulo ventaArticulo) {

        Object[] fila = new Object[7];
        fila[0] = ventaArticulo.getId();
        fila[1] = ventaArticulo.getArticuloCodigo();
        fila[2] = ventaArticulo.getArticuloDescripcion();
        fila[3] = new DecimalFormat("0.00").format(ventaArticulo.getCantidadPeso());
        fila[4] = ventaArticulo.getPrecioUnitario();
        fila[5] = new DecimalFormat("0.00").format(Double.parseDouble(ventaArticulo.getPrecioPromocion().toString()));
        fila[6] = new DecimalFormat("0.00").format(Double.parseDouble(ventaArticulo.getPrecio().toString()));
        modeloTablaDetalle.addRow(fila);
    }

    private void cargarEncabezadosTablaDetalle(ModeloTablaNoEditable modeloTablaDetalle) {
        modeloTablaDetalle.addColumn("Id");
        modeloTablaDetalle.addColumn("Codigo");
        modeloTablaDetalle.addColumn("Articulo");
        modeloTablaDetalle.addColumn("Peso Neto/Cantidad");
        modeloTablaDetalle.addColumn("Precio U.");
        modeloTablaDetalle.addColumn("Promo");
        modeloTablaDetalle.addColumn("SubTotal");
        tblDetalle.setModel(modeloTablaDetalle);
    }

    private void configurarTablaVentas(JTable tbl) {
        JViewport scroll = (JViewport) tbl.getParent();
        int ancho = scroll.getWidth();
        int anchoColumna = 0;
        TableColumnModel modeloColumna = tbl.getColumnModel();
        TableColumn columnaTabla;
        for (int i = 0; i < tbl.getColumnCount(); i++) {
            columnaTabla = modeloColumna.getColumn(i);
            switch (i) {
                case 0:
                    anchoColumna = (1 * ancho) / 100;
                    break;
                case 1:
                    anchoColumna = (20 * ancho) / 100;
                    break;
                case 2:
                case 3:
                case 4:
                case 5:
                case 6:
                case 7:
                case 8:
                case 9:
                case 10:
                case 11:
                case 12:
                    anchoColumna = (5 * ancho) / 100;
                    break;
            }
            columnaTabla.setPreferredWidth(anchoColumna);
            tbl.setColumnModel(modeloColumna);
        }
        tbl.getTableHeader().setFont(new java.awt.Font("Dialog",
                java.awt.Font.PLAIN, 10));
        tbl.getTableHeader().setBackground(java.awt.Color.WHITE);
        tbl.getTableHeader().setForeground(Color.BLACK);
        //Si le queremos cambiar el tamaño a la tablita
        tbl.setFont(new java.awt.Font("Dialog",
                java.awt.Font.PLAIN, 15));
    }
