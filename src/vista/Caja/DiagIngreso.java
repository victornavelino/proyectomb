    private boolean validarIngreso() {
        if (tfImporte.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe tipear un importe");
            tfImporte.grabFocus();
            return false;
        }
        if (tfDescripcion.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe tipear la descripcion");
            tfDescripcion.grabFocus();
            return false;
        }
        if (ftfHora.getText().equals("  :  ")) {
            JOptionPane.showMessageDialog(null, "Debe cargar la hora en formato HH:MM   ejemplo 16:30");
            ftfHora.grabFocus();
            return false;
        }
        if (Comunes.concatenarFechaConHora(dpFecha.getDate(), ftfHora.getText()) == null) {
            JOptionPane.showMessageDialog(null, "Debe cargar correctamente el formato ejemplo 16:30");
            ftfHora.grabFocus();
            return false;
        }
        return true;

    }

    private void cargarTablaIngreso(List<Ingreso> ingresos) {
        modeloTablaIngreso = new ModeloTablaNoEditable();
        cargarEncabezadosTablaIngreso(modeloTablaIngreso);
        configurarTabla(tblIngreso);
        try {
            cargarIngresos(ingresos);
        } catch (Exception ex) {
            Logger.getLogger(DiagIngreso.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void cargarEncabezadosTablaIngreso(ModeloTablaNoEditable modeloTablaIngreso) {
        modeloTablaIngreso.addColumn("Id");
        modeloTablaIngreso.addColumn("Fecha");
        modeloTablaIngreso.addColumn("Tipo de Ingreso");
        modeloTablaIngreso.addColumn("Importe");
        modeloTablaIngreso.addColumn("Observaciones");
        tblIngreso.setModel(modeloTablaIngreso);
    }

    private void configurarTabla(JTable tbl) {
        JViewport scroll = (JViewport) tbl.getParent();
        int ancho = scroll.getWidth();
        int anchoColumna = 0;
        TableColumnModel modeloColumna = tbl.getColumnModel();
        TableColumn columnaTabla;
        for (int i = 0; i < tbl.getColumnCount(); i++) {
            columnaTabla = modeloColumna.getColumn(i);
            switch (i) {
                case 0:
                    anchoColumna = (1 * ancho) / 100;
                    break;
                case 1:
                    anchoColumna = (20 * ancho) / 100;
                    break;
                case 2:
                case 3:
                case 4:
                case 5:
                case 6:
                case 7:
                case 8:
                case 9:
                case 10:
                case 11:
                case 12:
                    anchoColumna = (5 * ancho) / 100;
                    break;
            }
            columnaTabla.setPreferredWidth(anchoColumna);
            tbl.setColumnModel(modeloColumna);
        }
        tbl.getTableHeader().setFont(new java.awt.Font("Dialog",
                java.awt.Font.PLAIN, 10));
        tbl.getTableHeader().setBackground(java.awt.Color.WHITE);
        tbl.getTableHeader().setForeground(Color.BLACK);
        //Si le queremos cambiar el tamaño a la tablita
        tbl.setFont(new java.awt.Font("Dialog",
                java.awt.Font.PLAIN, 10));
    }

    private void cargarIngresos(List<Ingreso> ingresos) {
        try {
            modeloTablaIngreso = new ModeloTablaNoEditable();
            cargarEncabezadosTablaIngreso(modeloTablaIngreso);
            for (Ingreso g : ingresos) {
                cargarIngreso(g);
            }

            tblIngreso.setModel(modeloTablaIngreso);
            Comunes.setOcultarColumnasJTable(tblIngreso, 0);
        } catch (Exception ex) {
            Logger.getLogger(DiagIngreso.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void cargarIngreso(Ingreso ingreso) {
        Object[] fila = new Object[5];
        fila[0] = ingreso.getId();
        SimpleDateFormat formats = new SimpleDateFormat("dd/MM/yyyy");
        try {
            fila[1] = formats.format(ingreso.getFecha());
        } catch (Exception e) {

        }
        fila[2] = ingreso.getTipoDeIngreso();
        fila[3] = ingreso.getImporte();
        fila[4] = ingreso.getDescripcion();
        modeloTablaIngreso.addRow(fila);
    }

    private void cargarIngreso() {
        ingreso = (Ingreso) MovimientoCajaFacade.getInstance().buscar((Long) tblIngreso.getValueAt(tblIngreso.getSelectedRow(), 0));
        tfImporte.setText(String.valueOf(ingreso.getImporte()));
        tfDescripcion.setText(ingreso.getDescripcion());
        dpFecha.setDate(ingreso.getFecha());
        try {
            cbTipoIngreso.setSelectedItem(ingreso.getTipoDeIngreso());
        } catch (Exception e) {
        }
        try {
            ftfHora.setText(Comunes.extraerHoraDeFecha(ingreso.getFecha()));
        } catch (Exception e) {
        }
    }

    private void eliminarIngreso() {
        try {
            MovimientoCajaFacade.getInstance().eliminar(ingreso.getId());
            JOptionPane.showMessageDialog(null, "Registro Eliminado");
            cargarTablaIngreso(MovimientoCajaFacade.getInstance().getIngresos());
            limpiar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e);
        }
    }

    private void limpiar() {
        ingreso = new Ingreso();
        Comunes.cargarJCombo(cbTipoIngreso, GenericoFacade.getInstance().find("TipoDeIngreso"));
        tfDescripcion.setText("");
        tfImporte.setText("");
        ftfHora.setText("");
        btnAgregar.setEnabled(true);
        btnModificar.setEnabled(false);
        btnEliminar.setEnabled(false);
    }

