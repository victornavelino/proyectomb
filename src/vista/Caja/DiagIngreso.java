    private void inicializarComponentes() {
        caja = CajaFacade.getInstance().getCajaAbierta(sucursal, usuario);
        if (caja == null) {
            Comunes.mensajeError("Es posible que no se haya abierto caja", "No existe caja abierta");
            this.dispose();
        }  //CARGAR TABLA CON Ingresos
        cargarTablaIngreso(MovimientoCajaFacade.getInstance().getIngresos(caja.getFechaInicio()));
        Comunes.setOcultarColumnasJTable(tblIngreso, 0);
        //CARGAR COMBO LISTA EMPLEADOS
        cargarTipoDeIngreso();
        btnAgregar.setEnabled(true);
        btnModificar.setEnabled(false);
        btnEliminar.setEnabled(false);
    }

    private void altaIngreso() {
        if (validarIngreso() && cbTipoIngreso.getSelectedIndex() != -1) {
            try {
                ingreso = new Ingreso();
                ingreso.setTipoDeIngreso((TipoDeIngreso) cbTipoIngreso.getSelectedItem());
//                ingreso.setFecha(dpFecha.getDate());
                ingreso.setFecha(Comunes.concatenarFechaConHora(dpFecha.getDate(), ftfHora.getText()));
                ingreso.setImporte(new BigDecimal(tfImporte.getText()));
                ingreso.setDescripcion(tfDescripcion.getText());
                ingreso.setUsuario(usuario);
                ingreso.setSucursal(sucursal);
                MovimientoCajaFacade.getInstance().alta(ingreso);
                JOptionPane.showMessageDialog(null, "Datos Registrados");
                cargarTablaIngreso(MovimientoCajaFacade.getInstance().getIngresos(caja.getFechaInicio()));
                limpiar();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error: " + e);
            }
        }
    }

    private void cargarParaModificarIngreso() {
        cargarIngreso();
        btnAgregar.setEnabled(false);
        btnModificar.setEnabled(true);
        btnEliminar.setEnabled(true);
    }

    private void modificarIngreso() {
        if (validarIngreso() && cbTipoIngreso.getSelectedIndex() != -1) {
            try {
                ingreso.setTipoDeIngreso((TipoDeIngreso) cbTipoIngreso.getSelectedItem());
                ingreso.setFecha(Comunes.concatenarFechaConHora(dpFecha.getDate(), ftfHora.getText()));
                ingreso.setImporte(new BigDecimal(tfImporte.getText()));
                ingreso.setDescripcion(tfDescripcion.getText());
                ingreso.setUsuario(usuario);
                MovimientoCajaFacade.getInstance().modificar(ingreso);
                JOptionPane.showMessageDialog(null, "Datos modificados!!!");
                cargarTablaIngreso(MovimientoCajaFacade.getInstance().getIngresos());
                limpiar();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error: " + e);
            }
        }

    }

    private void filtrarIngresos() {
        if (cboFiltroTipoIngreso.getSelectedIndex() > 0) {
            cargarIngresos(MovimientoCajaFacade.getInstance().getIngresosPorTipo((TipoDeIngreso) cboFiltroTipoIngreso.getSelectedItem(), caja.getFechaInicio()));
        } else {
            cargarIngresos(MovimientoCajaFacade.getInstance().getIngresos(caja.getFechaInicio()));
        }

    }

