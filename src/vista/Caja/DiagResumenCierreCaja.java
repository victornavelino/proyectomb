    private void calcular() {
        if (!tfSaldoDeCajaReal.getText().isEmpty()) {
            calcularDiferenciaCaja();
        } else {
            tfDiferenciaCaja.setText("");
        }
    }

    private void calcularDiferenciaCaja() {
        if (!tfSaldoDeCajaReal.getText().isEmpty() && Comunes.validarBigDecimal(tfSaldoDeCajaReal.getText())) {
            if (new BigDecimal(tfSaldoDeCajaReal.getText().replace(",", ".")).signum() >= 0
                    && new BigDecimal(tfSaldoDeCaja.getText().replace(",", ".")).signum() >= 0) {
                //calculamos diferencia
                diferencia = new BigDecimal(tfSaldoDeCajaReal.getText().replace(",", "."))
                        .subtract(new BigDecimal(tfSaldoDeCaja.getText().replace(",", ".")));
                if (diferencia.signum() > -1) {
                    btnAceptar.setEnabled(true);
                    tfDiferenciaCaja.setForeground(Color.black);
                    tfDiferenciaCaja.setText(diferencia.toString());
                } else {
                    btnAceptar.setEnabled(false);
                    tfDiferenciaCaja.setForeground(Color.red);
                    tfDiferenciaCaja.setText(diferencia.toString());
                }

            }
        } else {
            tfDiferenciaCaja.setText("");
        }
    }

