    private void inicializarComponentes() {
        SimpleDateFormat formateador = new SimpleDateFormat("dd/MM/yyyy hh:mm:ss a");
        deshabilitarComponetes();
        try {
            tfSucursal.setText(caja.getSucursal().toString());
        } catch (Exception e) {
        }
        try {
            tfUsuario.setText(caja.getUsuario().toString());
        } catch (Exception e) {
        }
        try {
            tfFechaInicio.setText(formateador.format(caja.getFechaInicio()));
        } catch (Exception e) {
        }
        try {
            tfFechaCierre.setText(formateador.format(caja.getFechaFin()));
        } catch (Exception e) {
        }
        try {
            tfCajaInicial.setText(new DecimalFormat("0.000").format(caja.getCajaInicial()));
        } catch (Exception e) {
        }
        try {
            tfVentas.setText(new DecimalFormat("0.000").format(CajaFacade.getInstance().getCobroVentas(caja)));
        } catch (Exception e) {
        }
        try {
            //AQUI RESTAMOS DE LAAS COBRANZAS DE CTA CTE.. LAS COBRANZA EN CTA CTE COBRADAS CON TARJETA
            // YA QUE ESTAS ULTIMAS NO REPRESENTAN INGRESO DE CAJA
            BigDecimal cuponesCtaCteConTarjeta = new BigDecimal("0.000");
            BigDecimal cobranzaCtaCteConTarjeta;
            for (CuponTarjeta cupon : CuponTarjetaFacade.getInstance().getCuponesTarjetaCtaCteCaja(caja.getFechaInicio())) {
                cuponesCtaCteConTarjeta = cuponesCtaCteConTarjeta.add(cupon.getImporteCuponConRecargo());
            }
            cobranzaCtaCteConTarjeta = CajaFacade.getInstance().getCobranazaCtaCteCaja(caja);
            cobranzaCtaCteConTarjeta = cobranzaCtaCteConTarjeta.subtract(cuponesCtaCteConTarjeta);

            tfCobranzasCtaCte.setText(new DecimalFormat("0.000").format(cobranzaCtaCteConTarjeta));
        } catch (Exception e) {
        }
        try {
            tfIngresoVarios.setText(new DecimalFormat("0.000").format(CajaFacade.getInstance().getIngresosVarios(caja)));
        } catch (Exception e) {
        }
        try {
            tfTotal.setText(new DecimalFormat("0.000").format(CajaFacade.getInstance().getTotalIngresos(caja)));
        } catch (Exception e) {
        }
        try {
            tfSueldosYAdelantos.setText(new DecimalFormat("0.000").format(CajaFacade.getInstance().getSueldosyAdelantos(caja)));
        } catch (Exception e) {
        }
        try {
            tfGastos.setText(new DecimalFormat("0.000").format(CajaFacade.getInstance().getGastos(caja)));
        } catch (Exception e) {
        }
        try {
            tfRetirosEfectivo.setText(new DecimalFormat("0.000").format(CajaFacade.getInstance().getRetirosEfectivo(caja)));
        } catch (Exception e) {
        }
        try {
            tfTotalEgresos.setText(new DecimalFormat("0.000").format(CajaFacade.getInstance().getTotalEgresos(caja)));
        } catch (Exception e) {
        }
        try {
            //cuenta corriente
            for (CuentaCorriente cc : caja.getMovimientosCtaCte()) {
                totalCreditoCtaCte = totalCreditoCtaCte.add(cc.getImporteCtaCte());
            }
            tfTotalCuentaCorriente.setText(new DecimalFormat("0.000").format(totalCreditoCtaCte));
        } catch (Exception e) {
        }

        try {
            // TARJETA DE CREDITO y VALES(nuevo)
            for (CuponTarjeta cuponTarjeta : caja.getMovimientosCuponesTarjeta()) {
                if (!cuponTarjeta.getPlanTarjeta().getEsVale()) {
                    totalCuponesTarjeta = totalCuponesTarjeta.add(cuponTarjeta.getImporteCuponConRecargo());
                } else {
                    totalVale = totalVale.add(cuponTarjeta.getImporteCuponConRecargo());
                }

            }
            tfTotalTarjetaCredito.setText(new DecimalFormat("0.000").format(totalCuponesTarjeta));
            tfTotalVale.setText(new DecimalFormat("0.000").format(totalVale));
        } catch (Exception e) {
        }
        try {
            // saldo de caja
            tfSaldoDeCaja.setText(new DecimalFormat("0.000").format(CajaFacade.getInstance().getSaldoFinalCaja(caja)));
        } catch (Exception e) {
        }

    }

