    public void listarTodos() {
        configList = (List<Configuracion>) (java.beans.Beans.isDesignTime() ? java.util.Collections.emptyList() : configuracionFacade.getTodos());

        refrescarTabla();
    }

    public void refrescarTabla() {
        org.jdesktop.swingbinding.JTableBinding jTableBinding = org.jdesktop.swingbinding.SwingBindings.createJTableBinding(org.jdesktop.beansbinding.AutoBinding.UpdateStrategy.READ_WRITE, configList, tblaConfig);
        org.jdesktop.swingbinding.JTableBinding.ColumnBinding columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${parametro}"));
        columnBinding.setColumnName("Parametro");
        columnBinding.setColumnClass(String.class);
        columnBinding = jTableBinding.addColumnBinding(org.jdesktop.beansbinding.ELProperty.create("${valor}"));
        columnBinding.setColumnName("Valor");
        columnBinding.setColumnClass(String.class);
        bindingGroup.addBinding(jTableBinding);
        jTableBinding.bind();
        tblaConfig.addMouseListener(new java.awt.event.MouseAdapter() {
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                tblaConfigMouseClicked(evt);
            }
        });
        jScrollPane1.setViewportView(tblaConfig);
    }

    public void modificarConfiguracion() {
        confSeleccionado = configuracionFacade.buscar(tfParametroDespues.getText());
        if (confSeleccionado.getParametro().equals("descuentoempleado")) {
            if (Comunes.validarBigDecimal(tfValor.getText())) {
                confSeleccionado.setValor(tfValor.getText());
                configuracionFacade.modificar(confSeleccionado);
                listarTodos();
                JOptionPane.showMessageDialog(this, "Modificación realizada con éxito");
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Debe colocar valor numerico");
            }
        } else {
            confSeleccionado.setValor(tfValor.getText());
            configuracionFacade.modificar(confSeleccionado);
            listarTodos();
            JOptionPane.showMessageDialog(this, "Modificación realizada con éxito");
            this.dispose();
        }

    }

    private void backup() {
        try {
            if (System.getProperty("os.name").contentEquals("Linux")) {
                //comando backup postgres
                String cmd = "pg_dump -i -h localhost -p 5432 -U postgres -f /home/latradicion/back.backup carniceria";
                Runtime.getRuntime().exec(cmd);
                JOptionPane.showMessageDialog(null, "Backup de la base de datos realizado!!");
            } else {
                //comando windows
                String cmd = "c:\\Program Files\\PostgreSQL\\9.3\\bin\\pg_dump -i -h localhost -p 5432 -U postgres -f c:\\Users\\latradicion\\back.backup carniceria";
                Runtime.getRuntime().exec(cmd);
                JOptionPane.showMessageDialog(null, "Backup de la base de datos realizado!!");
            }

        } catch (IOException ioe) {
            System.out.println(ioe);
        }
    }

