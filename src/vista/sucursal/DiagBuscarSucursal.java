    private void cargarTablaArticulos(List<Sucursal> buscarPorNombre) {
        Comunes.cargarJList(jList1, buscarPorNombre);
    }

    private void seleccionarArticulo() {
        if (jList1.getSelectedIndex() != -1) {
            this.setSucursalBuscada((Sucursal) jList1.getSelectedValue());
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una Sucursal");
        }
    }
